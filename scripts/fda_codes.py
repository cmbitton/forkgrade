def code_weight(code: str) -> int:
    """Severity weight from FDA code string. Falls back to base code (no subsection)."""
    import re
    sev = CODE_SEVERITY.get(code)
    if sev is None:
        base = re.sub(r'\([A-Za-z]\)$', '', code)
        sev = CODE_SEVERITY.get(base)
    if sev == "P":
        return 3
    if sev == "Pf":
        return 2
    return 1
